public class Comparison_Java_Test {
    public static void main(String[] args) {
        int x = 5, y = 8;

        System.out.println(" x is " + x + " and y is " + y);
        System.out.println(" x == y result: " + (x == y));
        System.out.println(" x != y result: " + (x != y));
        System.out.println(" x > y result: " + (x > y));
        System.out.println(" x >= y result: " + (x >= y));
        System.out.println(" x < y result: " + (x < y));
        System.out.println(" x <= y result: " + (x <= y));

    }
}
